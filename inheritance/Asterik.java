public class Asterik {
    public static void main(String[] main) {
        int row = 1;
        int counter = 5;
        while (counter <= counter ) {
            int columna = 1;
            int columnb = 1;
            int patr = 5;
            while( columna <= row) {
                System.out.print("*");
                columna++;
            }
            while(patr < row){
            System.out.println(" ");
             --patr;
            }
            while(columnb < row) {
                System.out.print("*");
                columnb++;
            }
            System.out.println();
            row++;
            
        }
    }
}